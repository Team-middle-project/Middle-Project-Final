CREATE TABLE USER(
    USERID INT(100) UNSIGNED AUTO_INCREMENT NOT NULL PRIMARY KEY,
    ID VARCHAR(15) NOT NULL,
    PW VARCHAR(15) NOT NULL,
    user_address VARCHAR(255),
    AGE INT(10),
    user_name VARCHAR(50)
);

CREATE TABLE PRODUCT(
    PRODUCT_ID INT(100) UNSIGNED AUTO_INCREMENT NOT NULL PRIMARY KEY,
    USERID INT(100) UNSIGNED,
    PRODUCT_NAME VARCHAR(100) NOT NULL,
    PRODUCT_COST INT(10) NOT NULL,
    PRODUCT_DATE TIMESTAMP DEFAULT NOW(),
    PRODUCT_DESCRIPTION VARCHAR(100),
    PRODUCT_IMAGE VARCHAR(255),
    FOREIGN KEY (USERID) REFERENCES USER(USERID)
); 

